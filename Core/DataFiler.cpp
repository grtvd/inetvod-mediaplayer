/******************************************************************************/

DataFiler::DataFiler()
{
	fStreamPtr = MemoryStream::newInstance();
}

/******************************************************************************/

DataFiler::DataFiler(StreamPtr& streamPtr)
{
	fStreamPtr = streamPtr;
}

/******************************************************************************/

void DataFiler::resetForWrite()
{
	fStreamPtr->setSize(0);
	fStreamPtr->setPosition(0);
}

/******************************************************************************/

bool DataFiler::isMoreDataAvailable()
{
	return (((fStreamPtr->getSize() - fStreamPtr->getPosition()) > 0) ? TRUE : FALSE);
}

